<h1>Manual</h1>

<h3 id="intro">Introduction</h3>

<p>Welcome to Hearts2Go. This application helps you keep track of the scores in your games of Hearts.</p>

<h3 id="login">Registering and logging in</h3>

<p>We value simplicity and as such Hearts2Go allows you to play single games without needing to register.
	But if you do register, you get access to a few nice features.</p>
<p>You'll get your own profile page, including in-game avatar.
	We'll also keep track for you how many games you have played and how well you have done.</p>
<p>During registration we ask you what you like your username to be and what you would like your screenname to be.
	What's the difference between these two names? Well, your username is the name you use to log in on this application.
	Usernames need to be unique. But we also realize that those names usually doesn't look too charming once you're
	actually in-game ("xX_MaJiNBeJitA_Xx"), so we're also asking you to add a screenname. This is the name that is
	shown throughout the program.</p>
<p>The next time you return to Hearts2Go, you can just log in with your username and password. But what if you forget
	your password? That's why there's a (non-mandatory) email field during registration. Just press the 'I forgot my password'
	button and you'll get sent to a page where you can reset your password. Those to who have not added an email address
	to their account have no way to recover their passwords. Even if you would mail us, the creators, we would have no
	idea what it could be as your password is stored encrypted. So either make sure you never forget your password
	or just add an email address to your account. We promise we won't send you spam.</p>

<h3 id="newgame">Starting a new game</h3>

<p>In the New Game screen you can start a new game of Hearts.</p>